#define _CRT_SECURE_NO_WARNINGS 1

#include<stdio.h>

int main()
{
	int arr[5] = { 0 };
	int i = 0;
	for (i = 0; i < 5; i++)
	{
		scanf("%d", &arr[i]);
	}
	int sum = 0;
	for (i=0;i<5;i++)
	{
		sum = sum + arr[i];
	}
	printf("sum=%d\n",sum);//求出和
	int avg = sum / 5;//定义一个数值，和/5
	printf("avg=%d\n", avg);//求出/
	return 0;
}
//求五个数的平均值
